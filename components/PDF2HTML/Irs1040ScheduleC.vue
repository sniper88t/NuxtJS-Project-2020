<script>
import { formService } from '~/services/form-service'
import {
  IRS1040_UPDATED,
  IRSW2_UPDATED,
  SCHEDULEC_UPDATED
} from '~/events/event'
import MoneyInput from '~/components/Currency/MoneyInput'
import { stateList } from '~/services/states.js'
export default {
  name: 'Irs1040ScheduleC',
  components: { MoneyInput },
  data() {
    return {
      stateList,
      Irs1040ScheduleC: {
        BusinessUSAddress: {},
        OtherExpenseDetail: []
      },
      ExpenseDetail: {}
    }
  },
  mounted() {
    let firstName = ''
    let lastName = ''
    let ssn = ''
    if (this.$store.state.attachments.Irs1040) {
      firstName = this.$store.state.attachments.Irs1040.firstName ?? ''
      lastName = this.$store.state.attachments.Irs1040.lastName ?? ''
      ssn = this.$store.state.attachments.Irs1040.ssn ?? ''
    }
    this.Irs1040ScheduleC.ProprietorNm = firstName + ' ' + lastName
    this.Irs1040ScheduleC.SSN = ssn
    if (this.$store.state.attachments.Irsw2) {
      const ein = this.$store.state.attachments.Irsw2.EmployerEIN ?? ''
      this.Irs1040ScheduleC.EIN = ein
    }

    formService.$on(IRS1040_UPDATED, (data) => {
      this.Irs1040ScheduleC.SSN = data.ssn
      this.Irs1040ScheduleC.ProprietorNm = data.firstName + ' ' + data.lastName
      this.$forceUpdate()
    })
    formService.$on(IRSW2_UPDATED, (data) => {
      this.Irs1040ScheduleC.EIN = data.EmployerEIN
      this.$forceUpdate()
    })
  },
  methods: {
    line3Calculation() {
      const line1 = this.Irs1040ScheduleC.TotalGrossReceiptsAmt ?? 0
      const line2 = this.Irs1040ScheduleC.ReturnsAndAllowancesAmt ?? 0
      const line3 = line1 - line2
      this.Irs1040ScheduleC.NetGrossReceiptsAmt = line3
      this.$forceUpdate()
    },
    line5calculation() {
      // Gross profit.  Subtract line 4 from line 3
      const line3 = this.Irs1040ScheduleC.NetGrossReceiptsAmt ?? 0
      const line4 = this.Irs1040ScheduleC.CostOfGoodsSoldAmt ?? 0
      const line5 = line4 - line3
      this.Irs1040ScheduleC.GrossProfitAmt = line5
      this.$forceUpdate()
    },
    line7calculation() {
      // Gross income.  Add lines 5 and 6
      const line5 = this.Irs1040ScheduleC.GrossProfitAmt ?? 0
      const line6 = this.Irs1040ScheduleC.OtherIncomeAmt ?? 0
      const line7 = line5 + line6

        'EmployeeBenefitProgramAmt',
        'InsuranceAmt',
        'MortgageInterestPaidBanksAmt',
        'MortgageInterestPaidOtherAmt',
        'LegalAndProfessionalServiceAmt',
        'OfficeExpensesAmt',
        'PensionProfitSharingPlansAmt',
        'MachineryAndEquipmentRentAmt',
        'OtherBusinessPropertyRentAmt',
        'RepairsAndMaintenanceAmt',
        'SuppliesAmt',
        'TaxesAndLicensesAmt',
        'TravelAmt',
        'MealsAndEntertainmentAmt',
        'UtilitiesAmt',
        'WagesLessEmploymentCreditsAmt',
        'TotalOtherExpensesAmt'
      ]
      let result = 0
      Object.keys(this.Irs1040ScheduleC).map((line) => {
        if (!lines.includes(line)) return
        if (this.Irs1040ScheduleC[line]) result += this.Irs1040ScheduleC[line]
      })
      this.Irs1040ScheduleC.TotalExpensesAmt = result
      this.$forceUpdate()
    },
    line29calculation() {
      // Tentative profit or (loss). Subtract line 28 from line 7
      const line7 = this.Irs1040ScheduleC.GrossIncomeAmt ?? 0
      const line28 = this.Irs1040ScheduleC.TotalExpensesAmt ?? 0
      const line29 = line7 - line28
      this.Irs1040ScheduleC.TentativeProfitOrLossAmt = line29
      this.$forceUpdate()
    },
    line31calculation() {
      // Net profit or (loss).  Subtract line 30 from line 29.
      const line29 = this.Irs1040ScheduleC.TentativeProfitOrLossAmt ?? 0
      const line30 = this.Irs1040ScheduleC.HomeBusinessExpenseAmt ?? 0
      const line31 = line29 - line30
      this.Irs1040ScheduleC.NetProfitOrLossAmt = line31
      this.$forceUpdate()
    },
    line40calculation() {
      // Add lines 35 through 39
      const line35 = this.Irs1040ScheduleC.BeginningOfYearInventoryAmt ?? 0
      const line36 = this.Irs1040ScheduleC.PurchasesLessPersonalItemsAmt ?? 0
      const line37 = this.Irs1040ScheduleC.CostOfLaborAmt ?? 0
      const line38 = this.Irs1040ScheduleC.MaterialsAndSuppliesAmt ?? 0
      const line39 = this.Irs1040ScheduleC.OtherCostsAmt ?? 0
      const line40 = line35 + line36 + line37 + line38 + line39
      this.Irs1040ScheduleC.TotalCostsAmt = line40
      this.$forceUpdate()
    },
    line42calculation() {
      // Cost of goods sold.  Subtract line 41 from line 40. Enter the result here and on line 4
      const line40 = this.Irs1040ScheduleC.TotalCostsAmt ?? 0
      const line41 = this.Irs1040ScheduleC.EndOfYearInventoryAmt ?? 0
      const line42 = line40 - line41
      this.Irs1040ScheduleC.CostOfGoodsSoldAmt = line42
      this.$forceUpdate()
    },
    line48calculation() {
      const lines = [
        'Amt1',
        'Amt2',
        'Amt3',
        'Amt4',
        'Amt5',
        'Amt6',
        'Amt7',
        'Amt8',
        'Amt9'
      ]
      let result = 0
      Object.keys(this.ExpenseDetail).map((line) => {
        if (!lines.includes(line)) return
        if (this.ExpenseDetail[line]) result += this.ExpenseDetail[line]
      })
      this.Irs1040ScheduleC.TotalOtherExpensesAmt = result
      this.$forceUpdate()
    },
    addForm8829() {
      // Load form 8917
    },
    calculate() {
      this.line3Calculation()
      this.line5calculation()
      this.line7calculation()
      this.line28calculation()
      this.line29calculation()
      this.line29calculation()
      this.line31calculation()
      this.line40calculation()
      this.line42calculation()
      this.line48calculation()
    },
    onInputChange(evt) {
      this.calculate()
      formService.$emit(SCHEDULEC_UPDATED, this.Irs1040ScheduleC)
    }
  }
}
